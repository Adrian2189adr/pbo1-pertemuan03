package Switch;

public class Percabangan_Switch {
    public static void main(String[] args) {
        int jalanjalan=1;
        switch (jalanjalan){
            case 1:
                System.out.println("Japan");
                break;
            case 2:
                System.out.println("Germany");
                break;
            case 3:
                System.out.println("South Korea");
                break;
            case 4:
                System.out.println("Russia");
                break;
            default:
                System.out.println("Banjar");
                break;
        }
    }
}
